import { useEffect } from 'react';
import { useDashboardStore } from '../stores/dashboard.store';

export const useDashboardData = () => {
  const { stats, recentActivity, isLoading, error, fetchDashboardData } = useDashboardStore();

  useEffect(() => {
    fetchDashboardData();
  }, [fetchDashboardData]);

  return { stats, recentActivity, isLoading, error, refetch: fetchDashboardData };
};
